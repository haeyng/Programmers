import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String a = sc.next();
        char [] as = a.toCharArray();   
        
        for (int i = 0; i < as.length; i++) {
			System.out.println(as[i]);
		} //for
        
    }
}